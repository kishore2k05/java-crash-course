public class DataTypes {
    public static void main(String[] args) {
        // Java requires that the data type of a variable be explicitly stated.
        int number = 15; // The int data type is used to store whole numbers.
        System.out.println(number);
        char mychar = 'a'; // The char data type is used to store a single character. This character can be a letter, a digit, or a symbol.
        System.out.println(mychar);
        boolean equal = true; // For true/false values boolean data type is used.
        System.out.println(equal);
        String name = "Adam"; // The string data type is used to store sequence of characters.
        System.out.println(name);
    }
}
